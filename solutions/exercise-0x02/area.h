#pragma once

/**
 * @brief Calculate the area of a rectangle
 * 
 * @param width Width of the rectangle
 * @param height Height of the rectangle
 * @return double The calculated area
 */
double area(double width, double height);

/**
 * @brief Calculate the area of a circle with radius
 * 
 * @param radius Radius of the circle
 * @return double The calculated area
 */
double area(double radius);

/**
 * @brief Calculate the area of a triangle with side lengths
 * 
 * @param side1 Length of side 1
 * @param side2 Length of side 2
 * @param side3 Length of side 3
 * @return double The calculated area
 */
double area(double side1, double side2, double side3);